<?php

namespace App\Models;

class EnCeMomentModel extends Model{
    protected $id;
    protected $img;



    /**
     * Get the value of img
     */
    public function getImg()
    {
        return $this->img;
    }

    /**
     * Set the value of img
     *
     * @return  self
     */
    public function setImg($img)
    {
        $this->img = $img;

        return $this;
    }
}